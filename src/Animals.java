
public class Animals {

//  attributes
	private String idAnimal = null;
	private String name = null;
	private int age = 0;
	private boolean species = true;
	private boolean gender = true;
	private String medicalCond = null;

//	produce the methods for getters and setters.
	
	public String getIdAnimal() {
		return idAnimal;
	}
	
	public void setIdAnimal(String idAnimal) {
		this.idAnimal = idAnimal;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public boolean isSpecies() {
		return species;
	}
	
	public void setSpecies(boolean species) {
		this.species = species;
	}
	
	public boolean isGender() {
		return gender;
	}
	public void setGender(boolean gender) {
		this.gender = gender;
	}
	
	public String getMedicalCond() {
		return medicalCond;
	}
	
	public void setMedicalCond(String medicalCond) {
		this.medicalCond = medicalCond;
	}
	
	
	

	
}
